<h3>Sobre a Universidade</h3>
<p>Fundada em 1965 como Faculdade de Filosofia, Ciências e Letras, a atual UninCor – Universidade Vale do Rio Verde tem sede no município de Três Corações, em Minas Gerais, e conta com mais quatro polos localizados nas cidades de Belo Horizonte, Betim, Caxambu e Pará de Minas, no mesmo estado. Sua missão é promover e disseminar o conhecimento para formar profissionais qualificados, aptos para o mercado de trabalho e comprometidos com a sociedade, o meio ambiente, a ética e a vida nos âmbitos local e regional.</p>

<br>
<br>

<p>Para alcançar os seus objetivos, a Faculdade UninCor oferta cursos superiores de alta qualidade, contando com mais de 20 opções nas áreas de Saúde, Exatas, Humanas, Sociais e Ambientais. Todos eles são ministrados por um excelente corpo docente, formado por professores competentes e que possuem vasto conhecimento e experiência na área acadêmica</p>



